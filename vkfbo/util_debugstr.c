{
    float draw_dim[4];
    float ofst_vtx[4];
    float ofst_uv [4];
    float col_fg [4];
    float col_bg [4];
} ubo_vs_instance_t;

static int                      s_draw_cnt = 0;
static ubo_vs_instance_t        s_vs_instance[MAX_INSTANCE_NUM];

